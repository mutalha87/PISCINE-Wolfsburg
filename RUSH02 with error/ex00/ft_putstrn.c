#include "ft_h.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstrn(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\n')
	{
		ft_putchar(str[i]);
		i++;
	}
}
